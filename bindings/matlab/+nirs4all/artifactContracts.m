function item = contract(id, schema, pythonSurface, portableClaim, optionalPayloadFields, requiredRegistryEntries, publishedConstants)
if nargin < 7
    publishedConstants = struct();
end
item = struct();
item.id = id;
item.schema = schema;
item.producer = 'full-python-nirs4all';
item.consumerLevel = metadataLevels();
item.pythonSurface = pythonSurface;
item.portableClaim = portableClaim;
item.optionalPayloadFields = optionalPayloadFields;
item.requiredRegistryEntries = requiredRegistryEntries;
item.publishedConstants = publishedConstants;
end

function levels = metadataLevels()
surfaces = nirs4all.runtimeSurfaces();
levels = struct();
for idx = 1:numel(surfaces)
    levels.(surfaces{idx}) = 'metadata';
end
end
