function item = controller(id, kind, label, operatorClasses, inputs, outputs, parameters, executionPath, composes, runtime)
item = struct();
item.id = id;
item.kind = kind;
item.domain = 'methods';
item.label = label;
item.operatorClasses = operatorClasses;
item.ports = struct('inputs', {inputs}, 'outputs', {outputs});
item.parameters = parameters;
item.runtime = runtime;
item.executionPath = executionPath;
item.composes = composes;
end

